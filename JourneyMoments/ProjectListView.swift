                            print("üè∑Ô∏è Project name tapped: \(project.name)")
                            startRenamingProject(project)
                        }) {
                            HStack {
                                Text(project.name)
                                    .font(.headline)
                                    .fontWeight(.semibold)
                                    .foregroundColor(.white)
                                
                                // üÜï Á∑®ÈõÜ„Ç¢„Ç§„Ç≥„É≥Ë°®Á§∫
                                Image(systemName: "pencil")
                                    .font(.caption)
                                    .foregroundColor(.gray)
                                    .opacity(0.7)
                                
                                Spacer()
                            }
                        }
                        .buttonStyle(PlainButtonStyle())
                        
                        HStack {
                            HStack(spacing: 4) {
                                Image(systemName: "video.fill")
                                    .font(.caption)
                                Text("\(project.segmentCount)s")
                                    .font(.caption)
                            }
                            .foregroundColor(.yellow)
                            
                            Spacer()
                            
                            Text(formatDate(project.createdAt))
                                .font(.caption)
                                .foregroundColor(.gray)
                        }
                    }
                    
                    // „Éú„Çø„É≥Áæ§ÔºàÊó¢Â≠ò„ÅÆ„Åæ„ÅæÔºâ
                    HStack(spacing: 12) {
                        // ÊíÆÂΩ±„Éú„Çø„É≥
                        Button {
                            print("üîµ Record button tapped: \(project.name)")
                            onOpenProject(project)
                        } label: {
                            HStack(spacing: 6) {
                                Image(systemName: "camera.fill")
                                    .font(.caption)
                                Text("Record")
                                    .font(.caption)
                                    .fontWeight(.medium)
                            }
                            .padding(.horizontal, 16)
                            .padding(.vertical, 8)
                            .background(Color.blue)
                            .foregroundColor(.white)
                            .cornerRadius(15)
                        }
                        .buttonStyle(PlainButtonStyle())
                        
                        // ÂÜçÁîü„Éú„Çø„É≥Ôºà„Çª„Ç∞„É°„É≥„Éà„Åå„ÅÇ„ÇãÂ†¥Âêà„ÅÆ„ÅøÔºâ
                        if project.segmentCount > 0 {
                            Button {
                                print("üî¥ Play button tapped: \(project.name)")
                                onPlayProject(project)
                            } label: {
                                HStack(spacing: 6) {
                                    Image(systemName: "play.fill")
                                        .font(.caption)
                                    Text("Play")
                                        .font(.caption)
                                        .fontWeight(.medium)
                                }
                                .padding(.horizontal, 16)
                                .padding(.vertical, 8)
                                .background(Color.red)
                                .foregroundColor(.white)
                                .cornerRadius(15)
                            }
                            .buttonStyle(PlainButtonStyle())
                        }
                        
                        Spacer()
                    }
                }
                .padding(16)
                .background(Color(.systemGray6).opacity(0.15))
                .cornerRadius(12)
                .overlay(
                    RoundedRectangle(cornerRadius: 12)
                        .stroke(Color.gray.opacity(0.3), lineWidth: 1)
                )
                .listRowBackground(Color.clear)
                .listRowSeparator(.hidden)
                .swipeActions(edge: .trailing) {
                    // üÜï ËøΩÂä†: ÂêçÂâçÂ§âÊõ¥„Ç¢„ÇØ„Ç∑„Éß„É≥
                    Button("Rename") {
                        print("üè∑Ô∏è Rename action: \(project.name)")
                        startRenamingProject(project)
                    }
                    .tint(.orange)
                    
                    Button("Delete") {
                        print("üîç Delete target: \(project.name)")
                        projectToDelete = project
                        showDeleteAlert = true
                    }
                    .tint(.red)
                }
            }
        }
        .background(Color.black)
        .scrollContentBackground(.hidden)
        .listStyle(PlainListStyle())
    }
    
    // üÜï ËøΩÂä†: ÂêçÂâçÂ§âÊõ¥Èñ¢ÈÄ£„ÅÆÈñ¢Êï∞
    private func startRenamingProject(_ project: Project) {
        projectToRename = project
        newProjectName = project.name  // ÁèæÂú®„ÅÆÂêçÂâç„ÇíÂàùÊúüÂÄ§„Å®„Åó„Å¶Ë®≠ÂÆö
        showRenameAlert = true
    }
    
    private func resetRenameState() {
        projectToRename = nil
        newProjectName = ""
        showRenameAlert = false
    }

    // formatDateÈñ¢Êï∞ÔºàÊó¢Â≠ò„ÅÆ„Åæ„ÅæÔºâ
    private func formatDate(_ date: Date) -> String {
        let formatter = DateFormatter()
        formatter.dateFormat = "MM/dd"
        return formatter.string(from: date)
    }
}

