            Spacer()
            
            Image(systemName: "video.circle")
                .font(.system(size: 80))
                .foregroundColor(.gray)
            
            Text("No Projects")
                .font(.title2)
                .fontWeight(.semibold)
                .foregroundColor(.white)
            
            Text("Create a new project to start\ncapturing 1-second videos!")
                .font(.body)
                .foregroundColor(.gray)
                .multilineTextAlignment(.center)
                .lineLimit(nil)
            
            Spacer()
        }
        .padding(.horizontal, 40)
    }
    
    // üîß ‰øÆÊ≠£: „Éó„É≠„Ç∏„Çß„ÇØ„Éà‰∏ÄË¶ßÔºàÂêçÂâçÂ§âÊõ¥Ê©üËÉΩËøΩÂä†Ôºâ
    private var projectListView: some View {
        List {
            ForEach(projects) { project in
                VStack(alignment: .leading, spacing: 12) {
                    // üÜï ‰øÆÊ≠£: „Éó„É≠„Ç∏„Çß„ÇØ„ÉàÊÉÖÂ†±ÔºàÂêçÂâç„Çø„ÉÉ„Éó„ÅßÁ∑®ÈõÜÂèØËÉΩÔºâ
                    VStack(alignment: .leading, spacing: 4) {
                        // üÜï „Éó„É≠„Ç∏„Çß„ÇØ„ÉàÂêç„Çí„Çø„ÉÉ„ÉóÂèØËÉΩ„Å´„Åô„Çã
